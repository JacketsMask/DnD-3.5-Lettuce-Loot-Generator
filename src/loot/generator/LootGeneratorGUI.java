    private void generateLootButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateLootButtonActionPerformed
        double level = Double.parseDouble(levelTextField.getText());
        if (level > 1) {
            System.out.print(level + " has been floored to ");
            level = Math.floor(level);
            System.out.println(level + ".");
        }
        ArrayList<Object> loot =
                crawler.generateLootFromTable(level, coinsCheckBox.isSelected(),
                goodsCheckBox.isSelected(), itemsCheckBox.isSelected());

        if (loot.isEmpty()) {
            resultsTextArea.setText(resultsTextArea.getText() + "\nNothing found.");
        } else {
            resultsTextArea.setText(resultsTextArea.getText() + "\nResults:\n");
            for (Object o : loot) {
                System.out.println(o);
                resultsTextArea.setText(resultsTextArea.getText() + "\n" + o.toString());
            }
            resultsTextArea.setText(resultsTextArea.getText() + "\n");
        }
        resultsTextArea.setVisible(true);
        System.out.println("Loot generation complete.");
    }//GEN-LAST:event_generateLootButtonActionPerformed

